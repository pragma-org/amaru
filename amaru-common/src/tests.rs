use crate::pool::{issuer_vkey_to_pool_id, PoolId};

#[test]
fn test_issuer_vkey_to_pool_id() {
    // cad3c900ca6baee9e65bf61073d900bfbca458eeca6d0b9f9931f5b1017a8cd6
    let issuer_vkey = [0xca, 0xd3, 0xc9, 0x00, 0xca, 0x6b, 0xae, 0xe9, 0xe6, 0x5b, 0xf6, 0x10, 0x73, 0xd9, 0x00, 0xbf, 0xbc, 0xa4, 0x58, 0xee, 0xca, 0x6d, 0x0b, 0x9f, 0x99, 0x31, 0xf5, 0xb1, 0x01, 0x7a, 0x8c, 0xd6];
    let pool_id = issuer_vkey_to_pool_id(&issuer_vkey);
    // 00beef0a9be2f6d897ed24a613cf547bb20cd282a04edfc53d477114
    let expected_pool_id = PoolId::new([0x00, 0xbe, 0xef, 0x0a, 0x9b, 0xe2, 0xf6, 0xd8, 0x97, 0xed, 0x24, 0xa6, 0x13, 0xcf, 0x54, 0x7b, 0xb2, 0x0c, 0xd2, 0x82, 0xa0, 0x4e, 0xdf, 0xc5, 0x3d, 0x47, 0x71, 0x14]);
    assert_eq!(pool_id, expected_pool_id);
}
